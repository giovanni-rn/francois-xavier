// Comment générer un nombre aléatoire en JavaScript ?

const random = Math.round(Math.random() * 10)
console.log(random) // Nombre aléatoire (entier) entre 0 et 10

// Math.random() : Méthode qui renvoie un nombre décimal entre 0 et 1 (ex: 0.762461)
// Math.round() : Méthode qui arrondi à l'entier le plus proche